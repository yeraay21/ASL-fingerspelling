"""## 3. Función de carga con split por sujeto

Cargamos train y test por separado desde el principio, usando sujetos distintos para cada uno.
Así es imposible que una imagen del sujeto de test acabe en el train por error.

| Split | Sujetos | Propósito |
|---|---|---|
| **Train** | subject-1, 2, 3, 4 (+ extra) | El modelo aprende con estas imágenes |
| **Validation** | 10% aleatorio del train | Monitorizar durante el entrenamiento |
| **Test** | subject-5 completo | Evaluación final — persona nunca vista |
"""

def load_subjects(data_dir, subjects, classes, img_size=64, include_extra=False):
    """
    Carga imágenes de los sujetos indicados.

    Parámetros:
        data_dir     : ruta raíz del dataset
        subjects     : lista de carpetas a cargar, ej. ['subject-1', 'subject-2']
        classes      : lista ordenada de letras ['A','B',...,'Z']
        img_size     : tamaño de resize en píxeles (cuadrado)
        include_extra: si True, añade la carpeta 'extra/' al conjunto

    Devuelve:
        X : array float32, shape (N, img_size, img_size, 3), valores [0, 1]
        y : array int32, shape (N,), etiquetas numéricas
    """
    images, labels = [], []
    folders = subjects[:]

    if include_extra:
        extra_path = os.path.join(data_dir, 'extra')
        if os.path.isdir(extra_path):
            folders.append('extra')
        else:
            print('⚠️  Carpeta extra/ no encontrada, se omite')

    for folder in folders:
        folder_path = os.path.join(data_dir, folder)
        if not os.path.isdir(folder_path):
            print(f'⚠️  No encontrado: {folder}, se omite')
            continue

        n_loaded = 0
        for cls in classes:
            cls_path = os.path.join(folder_path, cls)
            if not os.path.isdir(cls_path):
                continue
            for img_file in os.listdir(cls_path):
                img = cv2.imread(os.path.join(cls_path, img_file))
                if img is None:
                    continue
                img = cv2.resize(img, (img_size, img_size))
                img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
                images.append(img)
                labels.append(classes.index(cls))
                n_loaded += 1

        print(f'  ✓ {folder:<15} {n_loaded:>6} imágenes')

    X = np.array(images, dtype='float32') / 255.0
    y = np.array(labels, dtype='int32')
    return X, y

