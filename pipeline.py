# Verificar balance de clases en cada split
# El train y val deben estar balanceados (stratify lo garantiza).
# El test puede no estarlo si subject-5 tiene distinto número de imgs por letra.

def check_balance(y, name, classes):
    c = Counter(y)
    vals = [c.get(i, 0) for i in range(len(classes))]
    mean = sum(vals) / len(vals)
    variacion = (max(vals) - min(vals)) / mean * 100
    print(f'{name:<15} min={min(vals):>5}  max={max(vals):>5}  '
          f'media={mean:>6.0f}  variación={variacion:.1f}%')

